class Product {
  final int id;
  final String ingredients;
  final String about;
  final double rating;
  final int calories;
  final String name;
  final String imagePath;
  final double price;
  final String category;

  Product({
    required this.id,
    required this.ingredients,
    required this.about,
    required this.calories,
    required this.rating,
    required this.name,
    required this.imagePath,
    required this.price,
    required this.category,
  });
}
